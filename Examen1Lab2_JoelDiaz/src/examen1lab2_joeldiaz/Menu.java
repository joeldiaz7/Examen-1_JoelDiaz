    private void jb_ingresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_ingresarMouseClicked
        boolean Q = false;
        for (int i = 0; i < users.size(); i++) {
            if (tf_usuario.getText().equals(users.get(i).getUser()) && tf_pass.getText().equals(users.get(i).getPass())) {
                jTabbedPane1.setEnabledAt(1, true);
                jTabbedPane1.setEnabledAt(2, true);
                i = users.size() + 1;
                Q = true;
            }
        }
        if (Q = false) {
            JOptionPane.showMessageDialog(this, "Nombre de usuario o contraseña inválidos");
        }

    }//GEN-LAST:event_jb_ingresarMouseClicked

