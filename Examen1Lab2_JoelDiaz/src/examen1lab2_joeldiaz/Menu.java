    private void jb_agregarNuevoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_agregarNuevoMouseClicked

        String nombre;
        String desc;
        int puntaje;
        int copias;
        int genero;
        double precio;
        String edicion;
        String autor;
        int publi;

        nombre = tf_name.getText();
        desc = ta_desc.getText();
        puntaje = Integer.valueOf(tf_puntuacion.getText());
        copias = Integer.valueOf(tf_copias.getText());
        genero = cb_genero.getSelectedItem().toString().charAt(0);
        precio = Double.valueOf(tf_precio.getText());
        edicion = tf_edicion.getText();
        autor = tf_autor.getText();
        publi = Integer.valueOf(tf_publi.getText());
        libro x = new libro(nombre, desc, puntaje, copias, genero, precio, edicion, autor, publi);
        libros.add(x);

        //ComboBox 1
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_Agregar.getModel();
        modelo.addElement(x);
        
        cb_Agregar.setModel(modelo);
        area_info.setText(libros.get(cb_Agregar.getSelectedIndex()).toString2());
        
        //ComboBox 2
        DefaultComboBoxModel modelo2 = (DefaultComboBoxModel) cb_libroModify.getModel();
        modelo2.addElement(x);
        cb_libroModify.setModel(modelo2);
        tf_modNombre.setText(libros.get(cb_libroModify.getSelectedIndex()).getNombre());
        ta_modDesc.setText(libros.get(cb_libroModify.getSelectedIndex()).getDesc());
        String t = "" + libros.get(cb_libroModify.getSelectedIndex()).getPuntaje();
        tf_modPunt.setText(t);
        String t2 = "" + libros.get(cb_libroModify.getSelectedIndex()).getCopias();
        tf_modCopias.setText(t2);
        String t3 = "" + libros.get(cb_libroModify.getSelectedIndex()).getPrecio();
        tf_modPrecio.setText(t3);
        tf_modEdicion.setText(libros.get(cb_libroModify.getSelectedIndex()).getEdicion());
        tf_modAutor.setText(libros.get(cb_libroModify.getSelectedIndex()).getAutor());
        String t4 = "" + libros.get(cb_Agregar.getSelectedIndex()).getPubli();
        tf_modPubli.setText(t4);

        tf_name.setText("");
        ta_desc.setText("");
        tf_puntuacion.setText("");
        tf_copias.setText("");
        tf_precio.setText("");
        tf_edicion.setText("");
        tf_autor.setText("");
        tf_publi.setText("");
        
        
        JOptionPane.showMessageDialog(this, "Agregado con exito");
        

    }//GEN-LAST:event_jb_agregarNuevoMouseClicked

